# TYpes of binary tree
# 1. Full binary tree - Every node can have 0 or 2 child
# 2. Complete Binary tree - All the levels except last level has 2 children, and last level have child in left to right side
# 3. Perfect Binary tree - All internal node have 2 child and all child are at same level
# 4. Balanced Binary tree - diff = | height of (left - right) subtree | should be less than 1 or equal to one
# 5. Pathological Binary tree (Degenerate Binary Tree)-  Every node contain only 1 child node

# Binary search tree - left contain data less than root and right child contain greater data
